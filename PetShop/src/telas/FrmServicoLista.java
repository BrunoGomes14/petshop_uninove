    private void carregaLista() {
       DefaultTableModel tblServicosModel = null;
       servicos = new RNServico().listarServico();
        
       tblServicosModel = (DefaultTableModel) tblServicos.getModel();
       tblServicosModel.getDataVector().clear();
       tblServicosModel.fireTableDataChanged();

        for (Servico servico : servicos) {
            tblServicosModel.addRow(new Object[]{
                servico.getId(),
                servico.getNome(),
                servico.getTempoMedio(),
                servico.getPreco(),
                "Editar"
            });
        } 
    }   
   
    
