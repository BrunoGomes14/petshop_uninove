    private void tblServicosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblServicosMouseClicked
        JTable source = (JTable) evt.getSource();
        int row = source.rowAtPoint(evt.getPoint());
        int column = source.columnAtPoint(evt.getPoint());
        
        switch (column) {
        // editar
            case 4:
                FrmServico frmServico = new FrmServico();
                frmServico.idServico = servicos.get(row).getId();
                frmServico.servico = servicos.get(row);
                frmServico.setVisible(true);
                break;
            default:
                break;
        }
    }//GEN-LAST:event_tblServicosMouseClicked

