    private void tblPetsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblPetsMouseClicked
        JTable source = (JTable) evt.getSource();
        int row = source.rowAtPoint(evt.getPoint());
        int column = source.columnAtPoint(evt.getPoint());
        
        switch (column) {
        // editar
            case 4:
                FrmPet pet = new FrmPet();
                pet.idPet = arrPets.get(row).getId();
                pet.idResponsavel = arrPets.get(row).getResponsavel().getId();
                pet.setVisible(true);
                break;
        // apagar
            case 5:
                excluirPet(row);
                break;
            default:
                break;
        }
    }//GEN-LAST:event_tblPetsMouseClicked

