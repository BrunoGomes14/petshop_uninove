    public void preencheClientes(){
        int posicaoAux = 0;
        arrClientes = new RNCliente().listarTodosUsuarios("");
        
        cboClientes.addItem("");

        for (Cliente clienteItem : arrClientes)
        {
            cboClientes.addItem(clienteItem.getCliente());
            
            if (idCliente == clienteItem.getId()){
                cboClientes.setSelectedIndex(posicaoAux + 1);   
                cboClientes.enable(false);
                btnVoltar.requestFocus();
            }
            
            posicaoAux++;
        }
     
    }

    public void preenchePets() {

        DefaultTableModel tblClientesModel = null;

        arrPets = new RNPets().listarPets(idCliente);
        tblClientesModel = (DefaultTableModel) tblPets.getModel();
        tblClientesModel.getDataVector().clear();
        tblClientesModel.fireTableDataChanged();

        for (Pet petItem : arrPets) {
            tblClientesModel.addRow(new Object[]{
                petItem.getId(),
                petItem.getNome(),
                petItem.getRga(),
                Util.dataToString(petItem.getDtNascimento()),
                "Editar",
                "Apagar"
            });
        }
    }
    
    public void excluirPet(int index){
        
        Pet pet = arrPets.get(index);
        
        JPanel panel = new JPanel();
        panel.add(new JLabel("Deseja realmente excluir o pet \""+pet.getNome()+"\"?"));

        int result = JOptionPane.showOptionDialog(null, panel, "Excluir Pet",
                JOptionPane.YES_NO_OPTION, JOptionPane.PLAIN_MESSAGE,
                null, null, null);
        
        if (result == JOptionPane.YES_OPTION){
            new RNPets().excluirPet(pet.getId());
        }
        
        preenchePets();
    }

