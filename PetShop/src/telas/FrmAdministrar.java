    private void tblUsuariosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblUsuariosMouseClicked
        JTable source = (JTable) evt.getSource();
        int row = source.rowAtPoint(evt.getPoint());
        int column = source.columnAtPoint(evt.getPoint());
        
        switch (column) {
        // apagar
            case 3:
                excluirUsuario(row);
                break;
            default:
                break;
        }
    }//GEN-LAST:event_tblUsuariosMouseClicked
    
    private void excluirUsuario(int row){
        
        JPanel panel = new JPanel();
        panel.add(new JLabel("Deseja realmente excluir o usuário?"));

        int result = JOptionPane.showOptionDialog(null, panel, "Excluir Pet",
                JOptionPane.YES_NO_OPTION, JOptionPane.PLAIN_MESSAGE,
                null, null, null);
        
        if (result == JOptionPane.YES_OPTION){
            new RNUsuario().excluirUsuario(arrUsuarios.get(row).getId());
            carregaDados();
        }
    }
    
    private void carregaDados()
    {
        lblDashboard.setText("Dashboard " + Util.dataToString(Calendar.getInstance().getTime()).substring(0, 5));
        RNCliente rnClientes = new RNCliente();
        ArrayList<Cliente> arrCliente = rnClientes.listarTodosUsuarios("");
        int iQtdClienteNovo = 0;
        
        for (Cliente cliente: arrCliente){
            if (Util.dataToString(cliente.getDtCadastro()).equals(Util.dataToString(Calendar.getInstance().getTime()))){
                iQtdClienteNovo += 1;
            }
        }
        
        DefaultTableModel tblUsuariosModel = null;
        tblUsuariosModel = (DefaultTableModel) tblUsuarios.getModel();
        tblUsuariosModel.getDataVector().clear();
        tblUsuariosModel.fireTableDataChanged();
        
        arrUsuarios = new RNUsuario().listarTodosUsuarios();
        
        for (Usuario usuario : arrUsuarios)
        {
            tblUsuariosModel.addRow(new Object[]{
                usuario.getId(),
                usuario.getUsuario(),
                usuario.getCargo(), 
                "Apagar"
            });
        }
        
        lblClientesNovos.setText(String.valueOf(iQtdClienteNovo));
        lblClientes.setText(String.valueOf(arrCliente.size()));
    }
    
